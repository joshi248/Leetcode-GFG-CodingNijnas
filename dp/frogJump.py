def find(dp, heights, index):
    if index == 0:
        dp[index] = 0
    if dp[index] != -1:
        return dp[index]
    left = find(dp, heights, index - 1) + abs(heights[index] - heights[index-1])
    right = float('inf')
    if index > 1:
        right = find(dp, heights, index - 2) + abs(heights[index] - heights[index-2])
    dp[index] = min(left, right)
    return dp[index]

def solve():
    n = int(input())
    heights = list(map(int, input().split(' ')))
    dp = [-1 for i in range(n+1)]
    find(dp, heights, n-1)
    print(dp[n-1])

def main():
    t = int(input())
    for i in range(t):
        solve()
